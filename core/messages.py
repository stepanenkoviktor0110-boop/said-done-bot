def format_tasks(tasks: list[str]) -> str:
    return "\n".join(f"{i + 1}. {t}" for i, t in enumerate(tasks))


def failure_note(failed: int, total: int) -> str:
    return f"\n\n⚠️ {failed} из {total} не удалось обработать."


def survey_q(index: int) -> str:
    return f"Вопрос {index + 1}/4: {SURVEY_QUESTIONS[index]}"
